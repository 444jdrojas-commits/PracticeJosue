    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
       int fila = jTable1.getSelectedRow();
        if (fila == -1) {
            javax.swing.JOptionPane.showMessageDialog(this, "Por favor, selecciona un producto de la tabla.");
            return;
        }

        int confirmacion = javax.swing.JOptionPane.showConfirmDialog(this, "¿Estás seguro de eliminar este producto?", "Confirmar", javax.swing.JOptionPane.YES_NO_OPTION);
        
        if (confirmacion == javax.swing.JOptionPane.YES_OPTION) {
            try {
                // Obtenemos el ID de la primera columna de la fila seleccionada
                String idStr = jTable1.getValueAt(fila, 0).toString();
                
                ec.edu.espe.practica.controller.ProductController controller = new ec.edu.espe.practica.controller.ProductController();
                
                // Llamamos a un método de borrado (lo agregaremos al controlador abajo)
                controller.eliminar(idStr);
                
                javax.swing.JOptionPane.showMessageDialog(this, "Producto eliminado correctamente.");
                refreshTable(); // Actualizar la tabla
                
            } catch (Exception e) {
                javax.swing.JOptionPane.showMessageDialog(this, "Error al eliminar: " + e.getMessage());
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnUploadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUploadActionPerformed
        try {
            String id = txtId.getText();
            String desc = txtDescription.getText();
            double price = Double.parseDouble(txtPrice.getText());

            ec.edu.espe.practica.controller.ProductController controller = new ec.edu.espe.practica.controller.ProductController();
            
            // Llamamos al método de actualizar (lo agregaremos al controlador abajo)
            controller.actualizar(id, desc, price);
            
            javax.swing.JOptionPane.showMessageDialog(this, "Producto actualizado con éxito.");
            refreshTable();
            
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Error al actualizar: " + e.getMessage() + "\n(Asegúrate de poner el ID correcto)");
        }
    }//GEN-LAST:event_btnUploadActionPerformed

