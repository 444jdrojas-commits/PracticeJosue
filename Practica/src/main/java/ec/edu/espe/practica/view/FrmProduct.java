    private void btnCalculateTotalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalculateTotalActionPerformed
        double sumaTotal = 0;
        // Recorre la columna 3 (Total w/Taxes) de la tabla
        for (int i = 0; i < jTable1.getRowCount(); i++) {
            sumaTotal += Double.parseDouble(jTable1.getValueAt(i, 3).toString());
        }
        JOptionPane.showMessageDialog(this, "Suma total del inventario: $" + String.format("%.2f", sumaTotal));
    }//GEN-LAST:event_btnCalculateTotalActionPerformed

