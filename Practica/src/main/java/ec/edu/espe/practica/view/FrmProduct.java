    public void refreshTable() {
        try {
            ec.edu.espe.practica.controller.ProductController control = new ec.edu.espe.practica.controller.ProductController();
            java.util.List<org.bson.Document> lista = control.obtenerTodos();
            
            javax.swing.table.DefaultTableModel modelo = (javax.swing.table.DefaultTableModel) jTable1.getModel();
            modelo.setRowCount(0); // Borra lo que haya en la tabla

            for (org.bson.Document doc : lista) {
                // Sacamos el valor guardado en la nube
                double totalConIva = doc.getDouble("tax_included_price");
                
                // RECÁLCULO: (Total / 1.15) para mostrar el precio base original
                double baseRecalculada = totalConIva / 1.15;

                modelo.addRow(new Object[]{
                    doc.get("_id"),
                    doc.get("description"),
                    String.format("%.2f", baseRecalculada),
                    String.format("%.2f", totalConIva)
                });
            }
        } catch (Exception e) {
            System.err.println("Error al cargar datos: " + e.getMessage());
        }
    }

