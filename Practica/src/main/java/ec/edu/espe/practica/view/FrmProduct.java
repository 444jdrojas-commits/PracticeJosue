    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
       try {
            ec.edu.espe.practica.controller.ProductController controller = new ec.edu.espe.practica.controller.ProductController();
            
            // Atrapamos los datos de los JTextFields
            String id = txtId.getText();
            String desc = txtDescription.getText();
            double price = Double.parseDouble(txtPrice.getText());
            
            // Guardamos
            controller.guardar(id, desc, price);
            
            javax.swing.JOptionPane.showMessageDialog(this, "Producto guardado en tienda_db!");
            
            // Limpiamos los campos
            txtId.setText("");
            txtDescription.setText("");
            txtPrice.setText("");
            
            refreshTable(); // Llama a la función de abajo para actualizar la tabla
            
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
    }//GEN-LAST:event_btnSaveActionPerformed

